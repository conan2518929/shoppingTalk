package com.example.shoppingtalk.info;

public class NewVersionInfo {

	private String ver;
	private String compulsive;
	private String url;

	public String getVer() {
		return ver;
	}

	public void setVer(String ver) {
		this.ver = ver;
	}

	public String getCompulsive() {
		return compulsive;
	}

	public void setCompulsive(String compulsive) {
		this.compulsive = compulsive;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
